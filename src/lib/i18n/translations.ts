const translations: Record<string, Record<string, string>> = { en, zh, ja, ko, es, fr, de };

/** Treat empty / whitespace as missing so EN fallback works for broken ZH entries. */
function pick(locale: string, key: string): string | undefined {
  const bag = translations[locale];
  if (!bag) return undefined;
  const v = bag[key];
  if (v == null) return undefined;
  if (typeof v === 'string' && v.trim() === '') return undefined;
  return v;
}

export function getTranslation(
  key: string,
  locale: string,
  params?: Record<string, string | number>,
): string {
  const requested = (locale || 'en').toLowerCase().split('-')[0];
  const loc = translations[requested] ? requested : 'en';
  let value = pick(loc, key) ?? pick('en', key) ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      value = value.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
  }
  return value;
}

export function detectBrowserLocale(): string {
  if (typeof window === 'undefined') return 'en';
  const lang = navigator.language?.split('-')[0]?.toLowerCase() ?? 'en';
  return translations[lang] ? lang : 'en';
}

export function hasTranslationKey(key: string, locale = 'en'): boolean {
  return pick(locale, key) != null || pick('en', key) != null;
}
